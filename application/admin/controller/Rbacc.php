    /**
     * 获取指定实例
     * @param $objstr
     * @return Object
     */
    public function getModel($objstr=''){
        if(empty($objstr))
           return false;
        $obj=lcfirst($objstr).'Instance';
        if(empty(self::$obj)){
            $tmpObj = new \ReflectionClass(self::$modelpath.ucfirst($objstr));
            self::$$obj=new $tmpObj->name();
        }
        return self::$$obj;
   }

