    private function saveCommon($modelname='',$msg=''){
        // 获取操作方法
        $method=lcfirst($modelname).'Handle';
        //获取保存数据方法
        $save='save'.$modelname;
        $commonData=self::getRbacl()->$method();
        $result=self::getModel($modelname)->$save($commonData);
        if(empty($result))
            outputJson('-2','保存失败');
        putlog($msg.'保存成功');
        outputJson('1','保存成功');
    }

