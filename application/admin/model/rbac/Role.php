<?php
namespace app\admin\model\rbac;
use app\common\model\BasicModel;
use think\Validate;

class Role extends BasicModel
{
    protected $table=[

    ];
    protected $field = true;
    protected function initialize()
    {
        parent::initialize(); // TODO: Change the autogenerated stub
    }

    /**
     * @param array $condition
     * @param int $limit
     * @param int $page
     * @param string $fileds
     * @return mixed
     */
    public function listRole($condition=[],$limit=10,$page=1,$fileds='*'){
        return $this->getInfo($this,$fileds,$condition,$limit,$page);
    }

    /*
     * 获取全部角色名称
     */
    public function listallRole($condition = [])
    {
        return parent::getAllInfo($this,$condition); // TODO: Change the autogenerated stub
    }

    /**
     * 角色数据保存
     * @param array $data  角色信息
     *                  里面的type属性  add----新增，  edit----编辑
     * @return bool
     */
    public function saveRole($data){
        if(empty($data['type']))
            return false;
        $type=$data['type'];
        unset($data['type']);
        //编辑
        if($type=='edit')
            return $this->saveInfo($this,$data,['r_id'=>$data['r_id']]);
        //新增
        return $this->saveInfo($this,$data);
    }
}